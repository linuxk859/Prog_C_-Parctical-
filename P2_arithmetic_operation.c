#include <stdio.h>

int main() {
    
    int a = 10, b = 5;
    int sum, difference, multiplication, division;

    // Arithmetic operations
    sum = a + b;
    difference = a - b;
    multiplication = a * b;
    division = a / b;

    // Display results
    printf("Sum: %d\n", sum);
    printf("Difference: %d\n", difference);
    printf("Multiplication: %d\n", multiplication);
    printf("Division: %d\n", division);

    return 0;
}
